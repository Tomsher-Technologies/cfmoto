            <p>CFMOTO THUNDER RACING TEAM's Antanas Kanopkinas concluded the challenging Dakar Rally with an impressive performance, securing the 5th position in the QUAD class in The Dakar Rally, renowned as one of the world's toughest events, spanned from January 5th to January 19th, covering 15 days and 12 official stages. This marked CFMOTO's debut in the Dakar Rally, and Antanas, driving a modified CFORCE 1000, showcased a new record for the best performance by a Chinese brand in this class with a total time of 113 hours, 25 minutes, and 50 seconds.<img src="{{ asset('assets/images/1.jpg') }}" alt=""></p>
            <p>Despite being rookies in both the pilot and the manufacturer, Antanas and CFMOTO still demonstrated exceptional capabilities, building on Antanas's previous achievements in the W2RC World Cross-Country Rally Championship and the Abu Dhabi Desert Challenge. In 2023, CFMOTO THUNDER RACING TEAM had already competed in the W2RC with CFORCE 1000 and secured a podium finish in the Abu Dhabi Desert Challenge. These achievements fueled the enthusiasm of CFMOTO THUNDER RACING TEAM and Antanas to showcase their strength in one of the world's most dangerous races, highlighting the robustness of the CFMOTO CFORCE 1000.<img src="{{ asset('assets/images/2.jpg') }}" alt=""></p>
            <p>The Dakar Rally organizers deliberately increased the difficulty in the initial stages, challenging Antanas and his CFMOTO vehicle. The CFORCE 1000, initially a conspicuous presence in the QUAD class, quickly gained recognition due to Antanas's outstanding performances. Overcoming challenges set by the organizers, Antanas secured the 6th position in the second stage, gradually improving the overall ranking.<img src="{{ asset('assets/images/3.jpg') }}" alt=""></p>
            <p>However, for a first-time participant, setbacks are inevitable to become stronger. In the 48-hour Chrono stage, due to running out of fuel, Antanas called for helicopter rescue just 3.7 km before the finish line. In the 11th stage, a rim shattered due to scattered stones, preventing him from completing the stage. Fortunately, the privileges of the Rally 2 category allowed him to continue participating in the subsequent stages.<img src="{{ asset('assets/images/4.jpg') }}" alt=""></p>
            <p>These achievements and experiences will undoubtedly make CFMOTO and CFMOTO THUNDER RACING TEAM even more formidable in future rallies. Anticipation is high for their continued impressive performances in the next 2024 W2RC races and other regional rallies attained will undoubtedly strengthen CFMOTO and CFMOTO THUNDER RACING TEAM for future rallies.<img src="{{ asset('assets/images/5.jpg') }}" alt=""></p>
            <p>As the only competitor of China from the Quad class in the Dakar Rally, CFMOTO becomes the first Chinese brand to break through numerous challenges. This devilish challenge perfectly showed the reflection of CFMOTO's 35 years of expertise in professional off-road research and development technology and robust capabilities. Confronting the challenging terrain of scattered rocks, dunes, and canyons, the CFORCE 1000 withstood these harsh tests. Over 100 hours of intense competition on the track, it unleashed its powerful off-road capabilities. This vehicle's high performance, superior off-road capability, and remarkable stability were thoroughly validated in this desert arena.<img src="{{ asset('assets/images/6.jpg') }}" alt=""></p>
            <p>The allure of the races and competitions lies in its challenges, and CFMOTO's performance in this Dakar Rally serves as the best testament to self-transcendence. A new adventure is about to set sail, we are all anticipating to witness CFMOTO's presence on the global stage alongside the world. It presents another fearless challenge, showcasing the unwavering determination of China's off-road prowess to the world.<img src="{{ asset('assets/images/7.jpg') }}" alt=""></p>
